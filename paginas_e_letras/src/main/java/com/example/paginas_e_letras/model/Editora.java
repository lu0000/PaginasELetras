package com.example.paginas_e_letras.model;

import java.util.List;

import jakarta.persistence.*;

@Entity
@Table(name = "EDITORA")
public class Editora extends AbstractEntity<Long>{
    
    @Column(nullable = false)
    private String nome;

    @Column(nullable = false)
    private String telefone;

    @OneToMany(mappedBy = "editora")
    private List<Livro> livros;
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public String getTelefone() {
        return telefone;
    }
    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }
    public List<Livro> getLivros() {
        return livros;
    }
    public void setLivros(List<Livro> livros) {
        this.livros = livros;
    }
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + ((nome == null) ? 0 : nome.hashCode());
        result = prime * result + ((telefone == null) ? 0 : telefone.hashCode());
        result = prime * result + ((livros == null) ? 0 : livros.hashCode());
        return result;
    }
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (!super.equals(obj))
            return false;
        if (getClass() != obj.getClass())
            return false;
        Editora other = (Editora) obj;
        if (nome == null) {
            if (other.nome != null)
                return false;
        } else if (!nome.equals(other.nome))
            return false;
        if (telefone == null) {
            if (other.telefone != null)
                return false;
        } else if (!telefone.equals(other.telefone))
            return false;
        if (livros == null) {
            if (other.livros != null)
                return false;
        } else if (!livros.equals(other.livros))
            return false;
        return true;
    }
    @Override
    public String toString() {
        return "Editora [nome=" + nome + ", telefone=" + telefone + ", livros=" + livros + "]";
    }
    
    
}
